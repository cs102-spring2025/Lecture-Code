# Initial assignment of an integer
mystery_var = 42
print("mystery_var (initial):", mystery_var, ", type:", type(mystery_var))

# Reassigning a float to the same variable
mystery_var = 3.14
print("mystery_var (after reassignment to float):", mystery_var, ", type:", type(mystery_var))

# Reassigning a string to the same variable
mystery_var = "Now I'm a string!"
print("mystery_var (after reassignment to string): " + mystery_var + ", type:", type(mystery_var))

# Reassigning a list to the same variable
mystery_var = [1, 2, 3]
print("mystery_var (after reassignment to list):", mystery_var, ", type:", type(mystery_var))
